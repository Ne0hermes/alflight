/**
 * Obtient le nom usuel d'un aérodrome à partir de son code ICAO
 * @param {string} icao - Code ICAO
 * @returns {string} Nom usuel ou code ICAO si non trouvé
 */
export function getAirportName(icao) {
  return AIRPORT_NAMES[icao] || icao;
}

/**
 * Enrichit un objet aérodrome avec son nom usuel
 * @param {Object} airport - Objet aérodrome
 * @returns {Object} Aérodrome enrichi
 */
export function enrichAirportName(airport) {
  const customName = AIRPORT_NAMES[airport.icao];
  if (customName) {
    airport.displayName = customName;
    // Extraire la ville du nom si pas déjà présente
    if (!airport.city && customName.includes('-')) {
      airport.city = customName.split('-')[0];
    }
  } else {
    airport.displayName = airport.name || airport.icao;
  }
  return airport;
}